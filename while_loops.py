# While Loop Syntax

# while condition:
#    code....


# print 1-10

number = 1

while number <= 10:
    print(number)
    number = number + 1

# For even numbers
while number <= 500:
    if number % 2 == 0:
        print(number)
    number = number + 1

# For odd numbers
while number <= 500:
    if number % 2 != 0:
        print(number)
    number = number + 1
